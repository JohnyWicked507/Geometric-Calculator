package src.config;

public interface CalculatorInterface {
	/**
	 * The method `calculateArea` calculates the area of a geometric figure
	 * represented by an object implementing the `InterfaceConfig` interface.
	 *
	 * @return The method `calculateArea` returns a `double` value representing the
	 *         calculated area of the geometric figure.
	 */
	double calculateArea();

	/**
	 * The method `calculatePerimeter` calculates the perimeter of a geometric
	 * figure represented by an object implementing the `InterfaceConfig` interface.
	 *
	 * @return The method `calculatePerimeter` returns a `double` value representing
	 *         the calculated perimeter of the geometric figure.
	 */
	double calculatePerimeter();
}
